int numero1 = 10;
int numero2 = 5;

// Adição
int soma = numero1 + numero2;
Console.WriteLine("Soma: " + soma);

// Subtração
int subtracao = numero1 - numero2;
Console.WriteLine("Subtração: " + subtracao);

// Multiplicação
int multiplicacao = numero1 * numero2;
Console.WriteLine("Multiplicação: " + multiplicacao);

// Divisão
int divisao = numero1 / numero2;
Console.WriteLine("Divisão: " + divisao);

// Resto da divisão (Módulo)
int restoDivisao = numero1 % numero2;
Console.WriteLine("Resto da divisão: " + restoDivisao);


Console.WriteLine();// Espaço entre os exemplos


// Operadores de incremento e decremento
int contador = 5;

// Pré-incremento
int preIncremento = ++contador;
Console.WriteLine("Pré-incremento: " + preIncremento);

// Pós-incremento
int posIncremento = contador++;
Console.WriteLine("Pós-incremento: " + posIncremento);

// Pré-decremento
int preDecremento = --contador;
Console.WriteLine("Pré-decremento: " + preDecremento);

// Pós-decremento
int posDecremento = contador--;
Console.WriteLine("Pós-decremento: " + posDecremento);

